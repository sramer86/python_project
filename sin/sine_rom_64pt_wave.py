import matplotlib.pyplot as plt
import numpy as np

# ===================== 64点精准正弦数据 =====================
# 范围：-π/2 ~ π/2
# 格式：8位无符号数 0~255
# =============================================================
sine_data = [
    0x00, 0x00, 0x00, 0x01, 0x02, 0x03, 0x04, 0x06,
    0x08, 0x0A, 0x0C, 0x0E, 0x11, 0x13, 0x16, 0x1A,
    0x1D, 0x20, 0x24, 0x28, 0x2C, 0x30, 0x34, 0x39,
    0x3D, 0x41, 0x46, 0x4B, 0x4F, 0x54, 0x59, 0x5E,
    0x62, 0x67, 0x6C, 0x71, 0x75, 0x7A, 0x7F, 0x83,
    0x87, 0x8C, 0x90, 0x94, 0x98, 0x9C, 0xA0, 0xA3,
    0xA6, 0xAA, 0xAD, 0xAF, 0xB2, 0xB4, 0xB6, 0xB8,
    0xBA, 0xBC, 0xBD, 0xBE, 0xBF, 0xC0, 0xC0, 0xC0,
]

# X轴：地址 0~63
address = list(range(64))

# ===================== 绘图 =====================
plt.figure(figsize=(10, 5))
plt.plot(address, sine_data, 'b-', linewidth=2, markersize=6, marker='o', color='#2E86AB')
plt.grid(True, alpha=0.3)
plt.title("64-point Sine Wave ROM (-π/2 to π/2, 8-bit Unsigned)", fontsize=14)
plt.xlabel("ROM Address (0 ~ 63)", fontsize=12)
plt.ylabel("Output Value (0 ~ 255)", fontsize=12)
plt.ylim(-10, 265)  # 留出上下边距
plt.show()